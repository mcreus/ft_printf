#include "libftprintf.h"

int	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (!str[i])
	{
		ft_putstr("(null)");
		return (6);
	}
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
