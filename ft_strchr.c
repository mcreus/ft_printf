#include "libftprintf.h"

int	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == (char) c)
			return ((int *) s);
		s++;
	}
	if (*s == (char) c)
		return ((int *) s);
	return (0);
}
